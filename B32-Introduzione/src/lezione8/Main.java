/* Lezione 8
 * -La classe Math
 * 
 * - alcuni metodi: abs, min, max, pow, sqrt
 * - attributi: PI 
 */

package lezione8;

public class Main {

	public static void main(String[] args) {
		System.out.println(Math.abs(-55)); //valore assoluto
		
		System.out.println(Math.min(55, 21)); //Valore minimo tra due numeri
		
		System.out.println(Math.max(55, 21)); //Valore massimo tra due numeri
		
		System.out.println(Math.pow(5, 2)); //potenza del numero, "5 elevato a 2"
		
		System.out.println(Math.sqrt(25)); //square root, radice quadrata
		
		System.out.println(Math.PI); //pi greco
	}

}
